async def seed_behavior_knowledge() -> None:
    """
    將 BEHAVIOR_DOCS 預植入 ChromaDB。
    幂等性：已植入過（marker 存在）則直接 return，不重複嵌入。
    """
    # 先確認 embedding service 可用（get_embedding 回傳空 list 代表服務不通）
    from services.embedding_service import get_embedding as _probe
    probe_vec = await _probe("測試")
    if not probe_vec:
        logger.warning(
            "Embedding service unavailable — skipping behavior knowledge seeding."
        )
        return

    if _is_already_seeded():
        logger.info("Behavior knowledge already seeded (marker '%s' found), skipping.", _SEED_MARKER_ID)
        return

    logger.info("Starting behavior knowledge seeding (%d documents)...", len(BEHAVIOR_DOCS))

    seeded_count = 0
    for doc in BEHAVIOR_DOCS:
        try:
            n = await embed_document(
                doc_id=doc["doc_id"],
                filename=doc["filename"],
                chunks=[doc["content"]],
                metadatas=[{"category": "behavior_sop", "language": "zh-TW"}],
            )
            if n > 0:
                seeded_count += 1
                logger.info("Seeded behavior doc: %s", doc["doc_id"])
            else:
                logger.warning("Behavior doc '%s' produced 0 embedded chunks.", doc["doc_id"])
        except Exception as e:
            logger.warning("Failed to seed behavior doc '%s': %s", doc["doc_id"], e)

    # 植入 seed marker（使用一個短文字做標記，不影響搜尋品質）
