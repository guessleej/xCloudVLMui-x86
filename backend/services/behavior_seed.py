        await embed_document(
            doc_id=_SEED_MARKER_ID,
            filename="__seed_marker__.md",
            chunks=[f"[SEED MARKER] behavior-seed-v1 — {seeded_count} docs seeded"],
            metadatas=[{"category": "seed_marker", "language": "en"}],
        )
        logger.info(
            "Behavior knowledge seeding complete: %d/%d documents embedded.",
            seeded_count,
            len(BEHAVIOR_DOCS),
        )
