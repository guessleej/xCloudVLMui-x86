_SEED_MARKER_ID = "behavior-seed-v1"


def _is_already_seeded() -> bool:
    """檢查 ChromaDB 中是否已存在 seed marker，判斷是否已植入過。"""
    try:
        col = _get_collection()
        result = col.get(where={"doc_id": _SEED_MARKER_ID}, include=["documents"])
        return len(result.get("ids", [])) > 0
    except Exception as e:
        logger.warning("Could not check seed marker: %s", e)
        return False


